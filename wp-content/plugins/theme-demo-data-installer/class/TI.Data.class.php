<?php

/******************************************************************************/
/******************************************************************************/

class TIData
{
	/**************************************************************************/
	
	static function prepare()
	{
		global $ti_global;
		
		if(!is_array($ti_global)) $ti_global=array();		
	}
	
	/**************************************************************************/
	
	static function set($key,$name,$value)
	{
		global $ti_global;
		TIData::prepare();
		$ti_global[$key][$name]=$value;
	}

	/**************************************************************************/
}

/******************************************************************************/
/******************************************************************************/

?>